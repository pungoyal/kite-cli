#!/usr/bin/env node
/**
 * Generates docs/commands.md from the CLI's own `--help` output.
 *
 * Hand-maintaining a flag-by-flag reference for ~25 commands is a docs-rot
 * trap, and for a CLI that places real orders, a stale flag doc (wrong
 * --price/--quantity semantics, say) is actively dangerous. So this walks
 * `--help` recursively (root -> each command -> each subcommand, following
 * whichever ones themselves list a "Commands:" section) and renders exactly
 * what a user would see, instead of a second, hand-copied source of truth.
 *
 * `--check` regenerates in memory and diffs against the checked-in file,
 * exiting non-zero on drift — same shape as `npm run check:deps`.
 */
import { execFile } from 'node:child_process';
import { readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { promisify } from 'node:util';
import stripAnsi from 'strip-ansi';

const execFileAsync = promisify(execFile);

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const cli = join(root, 'dist', 'cli.js');
const outFile = join(root, 'docs', 'commands.md');

const env = {
  ...process.env,
  NO_COLOR: '1',
  KITE_CONFIG_DIR: join(root, 'node_modules', '.tmp', 'docs-config'),
  KITE_CACHE_DIR: join(root, 'node_modules', '.tmp', 'docs-cache'),
  KITE_DISABLE_KEYRING: '1',
};

async function help(args) {
  const { stdout } = await execFileAsync(process.execPath, [cli, ...args, '--help'], { env });
  return stripAnsi(stdout).trimEnd();
}

/** Command names from a "Commands:" block, excluding Commander's own `help`. */
function subcommandNames(helpText) {
  const match = helpText.match(/\nCommands:\n([\s\S]*?)(\n\n|$)/);
  if (!match) return [];
  const names = [];
  for (const line of match[1].split('\n')) {
    const m = line.match(/^ {2}(\S+)/);
    if (!m) continue;
    const name = m[1].split('|')[0]; // first alias, e.g. "authorise|authorize" -> "authorise"
    if (name !== 'help') names.push(name);
  }
  return names;
}

/**
 * Top-level command names, in the order they're grouped in `--help`.
 *
 * Root help has command-group sections (Account:, Portfolio:, ...) as well as
 * an Options: section and, after the command groups, Examples:/Safety: prose
 * sections that are not command listings at all — so this tracks the current
 * section header and only collects names while inside a real command group,
 * stopping entirely once Examples:/Safety: is reached.
 */
function topLevelNames(rootHelp) {
  const names = [];
  let inCommandSection = false;
  for (const line of rootHelp.split('\n')) {
    const header = line.match(/^([A-Za-z][A-Za-z ]*):$/);
    if (header) {
      if (header[1] === 'Examples' || header[1] === 'Safety') break;
      inCommandSection = header[1] !== 'Options';
      continue;
    }
    if (!inCommandSection) continue;
    const m = line.match(/^ {2}(\S+)/);
    if (!m) continue;
    const name = m[1].split('|')[0];
    if (name !== 'help' && !names.includes(name)) names.push(name);
  }
  return names;
}

async function renderCommand(path, depth) {
  const helpText = await help(path);
  const heading = '#'.repeat(Math.min(depth + 2, 6));
  const lines = [`${heading} \`kite ${path.join(' ')}\``, '', '```', helpText, '```', ''];

  const subs = subcommandNames(helpText);
  for (const sub of subs) {
    lines.push(...(await renderCommand([...path, sub], depth + 1)));
  }
  return lines;
}

async function generate() {
  const rootHelp = await help([]);
  const names = topLevelNames(rootHelp);

  const body = [];
  for (const name of names) {
    body.push(...(await renderCommand([name], 0)));
  }

  return [
    '<!-- auto-generated by scripts/generate-command-reference.mjs — do not edit by hand -->',
    '',
    '# Command reference',
    '',
    'Every command, flag, and subcommand exactly as `--help` prints it. Run',
    '`npm run docs:commands` to regenerate after adding or changing a command.',
    '',
    '## Global flags and environment variables',
    '',
    'See [configuration.md](configuration.md) for the full environment variable',
    'and config-key reference. The block below is `kite --help` itself:',
    '',
    '```',
    rootHelp,
    '```',
    '',
    "Exit codes are documented in the README's [Scripting](https://github.com/pungoyal/kite-cli#scripting) section.",
    '',
    ...body,
  ].join('\n');
}

async function main() {
  const check = process.argv.includes('--check');
  const content = `${(await generate()).trimEnd()}\n`;

  if (check) {
    let existing;
    try {
      existing = await readFile(outFile, 'utf8');
    } catch {
      existing = null;
    }
    if (existing !== content) {
      console.error(`docs/commands.md is out of date. Run \`npm run docs:commands\` and commit the result.`);
      process.exit(1);
    }
    console.log('docs/commands.md is up to date.');
    return;
  }

  await writeFile(outFile, content, 'utf8');
  console.log(`Wrote ${outFile}`);
}

main().catch((err) => {
  console.error(err instanceof Error ? err.message : err);
  process.exit(1);
});
